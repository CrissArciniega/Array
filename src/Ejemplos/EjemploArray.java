    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double notas[]=new double[3];
        double suma=0, promedio=0;
        
        notas[0]=Double.parseDouble(txtPrimerParcial.getText());
        notas[1]=Double.parseDouble(txtSegundoParcial.getText());
        notas[2]=Double.parseDouble(txtTercerParcial.getText());
      
        for (int i = 0; i < notas.length; i++) {
            suma=suma+notas[i];
        }
        
        promedio=suma/3;
        
        txtCalificacioFinal.setText(String.valueOf(promedio));
    }//GEN-LAST:event_btnCalcularActionPerformed

